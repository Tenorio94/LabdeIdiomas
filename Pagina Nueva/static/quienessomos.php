<h2>&iquest;Qui&eacute;nes Somos?</h2>
<p>
	El Centro de Recursos para el Aprendizaje de Idiomas del Campus Monterrey ofrece a sus usuarios una infraestructura f&iacute;sica y de servicios que concentra los recursos humanos, tecnol&oacute;gicos y did&aacute;cticos especialmente concebidos para apoyarlos en el logro de sus metas formativas relacionadas con el aprendizaje y la pr&aacute;ctica de los idiomas extranjeros.
</p>
<p>
	El Centro cuenta con tres &aacute;reas diferentes de atenci&oacute;n en donde se ofrecen servicios espec&iacute;ficos:
	<ul>
		<li>un Laboratorio de idiomas con 40 computadoras (CIAP 424) y diferentes programas computacionales espec&iacute;ficamente dise&ntilde;ados para aprender y practicar idiomas extranjeros.</li>
		<li>una Mediateca  (CIAP 423) con una creciente colecci&oacute;n de recursos did&aacute;cticos en diferentes idiomas.</li>
		<li>una Sala TOEFL (CIAP 422) en donde se alterna el aprendizaje autorregulado en ciertos horarios con la preparaci&oacute;n especifica para el TOEFL y otras pruebas estandarizadas en diferentes idiomas y formatos: impresos o en computadora.</li>
	</ul>
</p>
